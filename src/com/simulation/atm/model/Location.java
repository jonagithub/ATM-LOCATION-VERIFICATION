package com.simulation.atm.model;

public class Location {
	private String location;
	private String numberofcustomer;
	private int distanceofnearestatm;
	private int leasingrent;
	private String security;
	private String permissiblespace;
	private String marketingpotential;
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public String getNumberofcustomer() {
		return numberofcustomer;
	}
	public void setNumberofcustomer(String numberofcustomer) {
		this.numberofcustomer = numberofcustomer;
	}
	public int getDistanceofnearestatm() {
		return distanceofnearestatm;
	}
	public void setDistanceofnearestatm(int distanceofnearestatm) {
		this.distanceofnearestatm = distanceofnearestatm;
	}
	public int getLeasingrent() {
		return leasingrent;
	}
	public void setLeasingrent(int leasingrent) {
		this.leasingrent = leasingrent;
	}
	public String getSecurity() {
		return security;
	}
	public void setSecurity(String security) {
		this.security = security;
	}
	public String getPermissiblespace() {
		return permissiblespace;
	}
	public void setPermissiblespace(String permissiblespace) {
		this.permissiblespace = permissiblespace;
	}
	public String getMarketingpotential() {
		return marketingpotential;
	}
	public void setMarketingpotential(String marketingpotential) {
		this.marketingpotential = marketingpotential;
	}
	
}